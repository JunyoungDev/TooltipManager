package org.techtown.tooltiptest.tooltip;

public class Target {
    public int x;
    public int y;
    public int width;
    public int height;
    public String msg;

    public Target(int x, int y, int width, int height, String msg) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.msg = msg;
    }
}
